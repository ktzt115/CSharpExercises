double temperatureInFahrenheit = 32;
double multiplier = 5.0f / 9.0f;
Console.WriteLine($"Temperature In Fahrenheit is {temperatureInFahrenheit}\u00B0F");
double temperatureInCelsius = (temperatureInFahrenheit - 32) * multiplier;
Console.WriteLine($"Temperature In Celsius is {temperatureInCelsius}\u00B0C");